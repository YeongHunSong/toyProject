--H2
CREATE TABLE MEMBER
(
   MEMBER_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
   SIGNUP_DATE DATETIME,
   LOGIN_ID VARCHAR(30),
   PASSWORD VARCHAR(30),
   USERNAME VARCHAR(30),
   PRIMARY KEY (MEMBER_ID)
);

CREATE TABLE POST
(
   POST_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
   MEMBER_ID BIGINT,
   WRITING_DATE DATETIME,
   LAST_EDIT_DATE DATETIME,
   VIEW_COUNT BIGINT,
   POST_TITLE VARCHAR(100),
   POST_CONTENT VARCHAR(500),
   PRIMARY KEY (POST_ID)
);

CREATE TABLE COMMENT
(
   COMMENT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
   MEMBER_ID BIGINT,
   POST_ID BIGINT,
   WRITING_DATE DATETIME,
   LAST_EDIT_DATE DATETIME,
   COMMENT_CONTENT VARCHAR(300),
   PRIMARY KEY (COMMENT_ID)
);

CREATE TABLE POST_RECOMMEND
(
    POST_ID BIGINT,
    RECOMMENDER_ID BIGINT,
    RECOMMEND_DATE DATETIME
);

CREATE TABLE IMAGE
(
    IMAGE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    POST_ID BIGINT,
    ORIGIN_NAME VARCHAR(200),
    SERVER_NAME VARCHAR(200),
    FILE_SIZE BIGINT,
    UPLOAD_DATE DATETIME,
    PRIMARY KEY (IMAGE_ID)
);



